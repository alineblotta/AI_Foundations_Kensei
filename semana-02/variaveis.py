# 1. CRIANDO AS VARIÁVEIS (As caixas com etiquetas)
nome = "Aline"          # texto (sempre entre aspas)
idade = 31              # número inteiro (sem aspas)
altura = 1.65           # número decimal (usa ponto, não vírgula)
estudante = True        # verdadeiro ou falso (letra inicial maiúscula)

# 2. EXIBINDO OS VALORES (O comando print mostra o que tem na caixa)
print("Nome:", nome)
print("Idade:", idade)
print("Altura:", altura)
print("Estudante?", estudante)

# 3. DESCOBRINDO O TIPO (Dica do professor)
# O comando type() diz ao Python: "Que tipo de dado tem aqui dentro?"
print("O tipo da variável nome é:")
print(type(nome))